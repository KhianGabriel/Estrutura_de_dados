package AulasEstruturas.Arrays;

public class Exer01{

    public static void main(String[] args) {
        
        //Declaração de um array de inteiros
        int [] array = {2,3,4,5,6};

        //Impressão do array
        for (int i = 0; i < array.length; i++) {
            System.out.print(array[i] + " ");
        }
    }
}